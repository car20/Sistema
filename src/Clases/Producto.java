
package Clases;

public class Producto {
    protected String codigo;
    protected String nombre;

    public Producto(String codigo, String nombre) {
        this.codigo = codigo;
        this.nombre = nombre;
    }

    public String getCodigo() {
        return codigo;
    }

    public String getNombre() {
        return nombre;
    }
   
    

    
    }

    

  
    
    

